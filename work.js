/* ========================================
   ELEMENTS
======================================== */
const timelineContent = document.getElementById("timelineContent");
const activeYearHeading = document.getElementById("activeYearHeading");
const years = Array.from(document.querySelectorAll(".year"));
const timelineGlowDot = document.getElementById("timelineGlowDot");
const timelineProgress = document.getElementById("timelineProgress");
const timelineLine = document.querySelector(".timeline-line");

const yearOrder = ["present", "2025", "2024", "2023", "2022", "2021"];
const yearLabels = {
  present: "Present",
  2025: "2025",
  2024: "2024",
  2023: "2023",
  2022: "2022",
  2021: "2021",
};

let currentYearKey = "present";
let expandedByYear = {
  present: null,
  2025: null,
  2024: null,
  2023: null,
  2022: null,
  2021: null,
};

let sectionCenters = [];
let yearStops = [];
let scrollTicking = false;
let resizeTicking = false;

/* ========================================
   CARD TEMPLATE
======================================== */
function createCard(exp, yearKey, isOpen) {
  const skillsHtml = exp.skills
    .map((skill) => `<span>${skill}</span>`)
    .join("");
  const bulletsHtml = exp.bullets
    .map((bullet) => `<li>${bullet}</li>`)
    .join("");

  return `
    <article class="experience-card ${isOpen ? "is-open" : ""}" data-id="${exp.id}" data-year="${yearKey}">
      <button class="experience-toggle" type="button" aria-expanded="${isOpen}">
        <div class="experience-main">
          <h3>${exp.title}</h3>
          <p class="experience-company">${exp.company}</p>

          <div class="experience-meta">
            <span>${exp.tag}</span>
          </div>
        </div>

        <div class="experience-arrow" aria-hidden="true">
          <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.2" stroke-linecap="round" stroke-linejoin="round">
            <path d="M6 9l6 6 6-6"></path>
          </svg>
        </div>
      </button>

      <div class="experience-details">
        <div class="experience-details-inner">
          <div class="experience-details-content">
            <div class="detail-top-row">
              <div>
                <p class="detail-kicker">${exp.kicker}</p>
                <p class="detail-location">${exp.company} · ${exp.location}</p>
              </div>
              <span class="detail-date">${exp.date}</span>
            </div>

            <p class="detail-summary">${exp.summary}</p>

            <div class="detail-skills">
              ${skillsHtml}
            </div>

            <ul class="detail-list">
              ${bulletsHtml}
            </ul>
          </div>
        </div>
      </div>
    </article>
  `;
}

/* ========================================
   YEAR SECTION TEMPLATE
======================================== */
function createYearSection(yearKey) {
  const experiences = experienceByYear[yearKey] || [];

  return `
    <section class="year-section" id="section-${yearKey}" data-year="${yearKey}">
      <div class="year-section-header">
        <h2 class="year-section-heading">${yearLabels[yearKey] === "Present" ? "Present" : yearLabels[yearKey]}</h2>
        <span class="year-section-count">${experiences.length} ${experiences.length === 1 ? "role" : "roles"}</span>
      </div>

      <div class="year-cards">
        ${experiences
          .map((exp) =>
            createCard(exp, yearKey, expandedByYear[yearKey] === exp.id),
          )
          .join("")}
      </div>
    </section>
  `;
}

/* ========================================
   RENDER
======================================== */
function renderTimelineContent() {
  timelineContent.innerHTML = yearOrder.map(createYearSection).join("");
  attachCardEvents();
  cacheSectionMeasurements();
  updateScrollState();
}

/* ========================================
   CARD EVENTS
======================================== */
function attachCardEvents() {
  const toggles = document.querySelectorAll(".experience-toggle");

  toggles.forEach((toggle) => {
    toggle.addEventListener("click", () => {
      const card = toggle.closest(".experience-card");
      const yearKey = card.dataset.year;
      const cardId = card.dataset.id;

      expandedByYear[yearKey] =
        expandedByYear[yearKey] === cardId ? null : cardId;

      renderTimelineContent();

      const nextCard = document.querySelector(
        `.experience-card[data-year="${yearKey}"][data-id="${cardId}"]`,
      );

      if (nextCard && expandedByYear[yearKey] === cardId) {
        nextCard.scrollIntoView({
          block: "nearest",
          behavior: "smooth",
        });
      }
    });
  });
}

/* ========================================
   CACHE SECTION POSITIONS
======================================== */
function cacheSectionMeasurements() {
  const sections = yearOrder
    .map((yearKey) => document.getElementById(`section-${yearKey}`))
    .filter(Boolean);

  if (!sections.length || !timelineLine) return;

  sectionCenters = sections.map((section) => {
    const rect = section.getBoundingClientRect();
    return rect.top + window.scrollY + rect.height / 2;
  });

  yearStops = years.map((yearEl, index) => {
    const lineHeight = timelineLine.offsetHeight;
    const safeCount = Math.max(yearOrder.length - 1, 1);
    return (lineHeight / safeCount) * index;
  });
}

/* ========================================
   ACTIVE YEAR UI
======================================== */
function setActiveYear(yearKey) {
  currentYearKey = yearKey;
  activeYearHeading.textContent = yearLabels[yearKey];

  years.forEach((yearEl) => {
    yearEl.classList.toggle("active-year", yearEl.dataset.year === yearKey);
  });

  document.querySelectorAll(".year-section").forEach((section) => {
    section.classList.toggle(
      "active-section",
      section.dataset.year === yearKey,
    );
  });
}

/* ========================================
   DOT POSITION
======================================== */
function updateDotPosition() {
  if (window.innerWidth <= 1180 || !timelineLine || !sectionCenters.length) {
    timelineProgress.style.transform = `translateX(-50%) scaleY(0)`;
    timelineGlowDot.style.transform = `translate3d(-50%, 0px, 0)`;
    return;
  }

  const viewportCenter = window.scrollY + window.innerHeight / 2;

  let activeIndex = 0;
  let minDistance = Infinity;

  sectionCenters.forEach((center, index) => {
    const distance = Math.abs(center - viewportCenter);
    if (distance < minDistance) {
      minDistance = distance;
      activeIndex = index;
    }
  });

  setActiveYear(yearOrder[activeIndex]);

  let dotY = 0;

  if (viewportCenter <= sectionCenters[0]) {
    dotY = yearStops[0];
  } else if (viewportCenter >= sectionCenters[sectionCenters.length - 1]) {
    dotY = yearStops[yearStops.length - 1];
  } else {
    for (let i = 0; i < sectionCenters.length - 1; i += 1) {
      const currentCenter = sectionCenters[i];
      const nextCenter = sectionCenters[i + 1];

      if (viewportCenter >= currentCenter && viewportCenter <= nextCenter) {
        const localProgress =
          (viewportCenter - currentCenter) / (nextCenter - currentCenter);

        dotY = yearStops[i] + (yearStops[i + 1] - yearStops[i]) * localProgress;
        break;
      }
    }
  }

  const safeLineHeight = Math.max(timelineLine.offsetHeight, 1);
  const progressScale = Math.min(Math.max(dotY / safeLineHeight, 0), 1);

  timelineGlowDot.style.transform = `translate3d(-50%, ${dotY - 10}px, 0)`;
  timelineProgress.style.transform = `translateX(-50%) scaleY(${progressScale})`;
}

/* ========================================
   SCROLL STATE
======================================== */
function updateScrollState() {
  scrollTicking = false;
  updateDotPosition();
}

function requestScrollUpdate() {
  if (scrollTicking) return;
  scrollTicking = true;
  requestAnimationFrame(updateScrollState);
}

/* ========================================
   RESIZE
======================================== */
function handleResize() {
  if (resizeTicking) return;

  resizeTicking = true;

  requestAnimationFrame(() => {
    resizeTicking = false;
    cacheSectionMeasurements();
    updateDotPosition();
  });
}

/* ========================================
   YEAR CLICK SCROLL
======================================== */
years.forEach((yearEl) => {
  yearEl.addEventListener("click", () => {
    const targetYear = yearEl.dataset.year;
    const section = document.getElementById(`section-${targetYear}`);

    if (!section) return;

    section.scrollIntoView({
      behavior: "smooth",
      block: "center",
    });
  });
});

/* ========================================
   INIT
======================================== */
renderTimelineContent();

window.addEventListener("scroll", requestScrollUpdate, { passive: true });
window.addEventListener("resize", handleResize, { passive: true });
window.addEventListener("load", handleResize);

/* ========================================
   CUSTOM CURSOR GLOW + SOCIAL SIDEBAR
======================================== */
const cursorGlow = document.getElementById("cursorGlow");
const socialSidebar = document.querySelector(".social-sidebar");
const socialLinks = document.querySelectorAll(".social-sidebar a");

if (cursorGlow) {
  let mouseX = window.innerWidth / 2;
  let mouseY = window.innerHeight / 2;
  let currentX = mouseX;
  let currentY = mouseY;

  let targetWidth = 50;
  let targetHeight = 50;
  let currentWidth = 50;
  let currentHeight = 50;

  let pinnedToSidebar = false;
  let socialMorphTimer = null;
  let socialActive = false;

  const followSpeed = 0.22;
  const sizeSpeed = 0.18;

  window.addEventListener("mousemove", (event) => {
    if (!pinnedToSidebar) {
      mouseX = event.clientX;
      mouseY = event.clientY;
    }
  });

  function animateCursorGlow() {
    currentX += (mouseX - currentX) * followSpeed;
    currentY += (mouseY - currentY) * followSpeed;

    currentWidth += (targetWidth - currentWidth) * sizeSpeed;
    currentHeight += (targetHeight - currentHeight) * sizeSpeed;

    cursorGlow.style.width = `${currentWidth}px`;
    cursorGlow.style.height = `${currentHeight}px`;
    cursorGlow.style.transform = `translate3d(${currentX - currentWidth / 2}px, ${currentY - currentHeight / 2}px, 0)`;

    requestAnimationFrame(animateCursorGlow);
  }

  animateCursorGlow();

  /* ========================================
     GENERAL HOVER LOGIC
  ======================================== */
  document.querySelectorAll("a, button").forEach((item) => {
    item.addEventListener("mouseenter", () => {
      const isNavLink = item.classList.contains("nav-link");
      const isSocialLink = item.closest(".social-sidebar");

      if (isSocialLink) return;

      if (!pinnedToSidebar && !isNavLink) {
        targetWidth = 90;
        targetHeight = 90;
      }

      if (isNavLink) {
        cursorGlow.style.opacity = "0";
      }
    });

    item.addEventListener("mouseleave", () => {
      const isNavLink = item.classList.contains("nav-link");
      const isSocialLink = item.closest(".social-sidebar");

      if (isSocialLink) return;

      if (!pinnedToSidebar && !isNavLink) {
        targetWidth = 50;
        targetHeight = 50;
      }

      if (isNavLink) {
        cursorGlow.style.opacity = "0.9";
      }
    });
  });

  /* ========================================
     SOCIAL SIDEBAR MORPH
  ======================================== */
  if (socialSidebar && socialLinks.length) {
    let activeSocialLink = null;

    function getLinkCenterY(link) {
      const sidebarRect = socialSidebar.getBoundingClientRect();
      const rect = link.getBoundingClientRect();
      return rect.top - sidebarRect.top + rect.height / 2;
    }

    function getSidebarMetrics() {
      const sidebarRect = socialSidebar.getBoundingClientRect();
      const firstRect = socialLinks[0].getBoundingClientRect();
      const lastRect =
        socialLinks[socialLinks.length - 1].getBoundingClientRect();

      const firstCenterY =
        firstRect.top - sidebarRect.top + firstRect.height / 2;
      const lastCenterY = lastRect.top - sidebarRect.top + lastRect.height / 2;
      const centerY = (firstCenterY + lastCenterY) / 2;
      const fullHeight = lastCenterY - firstCenterY + 56;

      return {
        centerY,
        fullHeight,
      };
    }

    function startSidebarCircle(link) {
      const startY = getLinkCenterY(link);

      socialSidebar.classList.remove("is-pinned");
      socialSidebar.classList.add("is-circle");

      socialSidebar.style.setProperty("--social-pill-w", "74px");
      socialSidebar.style.setProperty("--social-pill-h", "74px");
      socialSidebar.style.setProperty("--social-pill-y", `${startY}px`);

      socialActive = true;
      pinnedToSidebar = true;

      targetWidth = 0;
      targetHeight = 0;
      cursorGlow.style.opacity = "0";
    }

    function expandSidebarPill() {
      if (!socialActive) return;

      const { centerY, fullHeight } = getSidebarMetrics();

      socialSidebar.classList.add("is-pinned");
      socialSidebar.style.setProperty("--social-pill-w", "74px");
      socialSidebar.style.setProperty("--social-pill-h", `${fullHeight}px`);
      socialSidebar.style.setProperty("--social-pill-y", `${centerY}px`);
    }

    function collapseSidebar() {
      clearTimeout(socialMorphTimer);

      socialActive = false;
      pinnedToSidebar = false;
      activeSocialLink = null;

      socialSidebar.classList.remove("is-circle");
      socialSidebar.classList.remove("is-pinned");

      socialSidebar.style.setProperty("--social-pill-w", "74px");
      socialSidebar.style.setProperty("--social-pill-h", "74px");

      targetWidth = 50;
      targetHeight = 50;
      cursorGlow.style.opacity = "0.9";
    }

    socialLinks.forEach((link) => {
      link.addEventListener("mouseenter", () => {
        activeSocialLink = link;

        clearTimeout(socialMorphTimer);
        startSidebarCircle(link);

        socialMorphTimer = setTimeout(() => {
          expandSidebarPill();
        }, 90);
      });
    });

    socialSidebar.addEventListener("mouseleave", () => {
      collapseSidebar();
    });
  }
}
